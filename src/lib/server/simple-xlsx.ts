type XlsxCellValue = string | number

function escapeXml(value: string) {
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&apos;")
}

function columnName(index: number) {
  let name = ""
  let value = index + 1
  while (value > 0) {
    const remainder = (value - 1) % 26
    name = String.fromCharCode(65 + remainder) + name
    value = Math.floor((value - 1) / 26)
  }
  return name
}

function buildSheetXml(rows: XlsxCellValue[][]) {
  const sheetRows = rows.map((row, rowIndex) => {
    const rowNumber = rowIndex + 1
    const cells = row.map((value, cellIndex) => {
      const ref = `${columnName(cellIndex)}${rowNumber}`
      if (typeof value === "number" && Number.isFinite(value)) {
        return `<c r="${ref}"><v>${value}</v></c>`
      }
      return `<c r="${ref}" t="inlineStr"><is><t>${escapeXml(String(value ?? ""))}</t></is></c>`
    }).join("")
    return `<row r="${rowNumber}">${cells}</row>`
  }).join("")

  const columnWidths = rows[0]?.map((_, index) => {
    const maxLength = rows.reduce((max, row) => Math.max(max, String(row[index] ?? "").length), 0)
    const width = Math.min(Math.max(maxLength + 2, 10), 48)
    return `<col min="${index + 1}" max="${index + 1}" width="${width}" customWidth="1"/>`
  }).join("") || ""

  return `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<worksheet xmlns="http://schemas.openxmlformats.org/spreadsheetml/2006/main" xmlns:r="http://schemas.openxmlformats.org/officeDocument/2006/relationships">
  <cols>${columnWidths}</cols>
  <sheetData>${sheetRows}</sheetData>
</worksheet>`
}

