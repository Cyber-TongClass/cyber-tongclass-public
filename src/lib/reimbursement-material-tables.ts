function normalizeSlug(value?: string) {
  return String(value || "")
    .trim()
    .toLowerCase()
    .replace(/[\s_]+/g, "-")
    .replace(/[^a-z0-9\u4e00-\u9fa5-]/g, "")
    .replace(/-+/g, "-")
    .replace(/^-|-$/g, "")
}

function normalizeCell(value: unknown) {
  return String(value ?? "").trim()
}

function isSectionCells(cells: string[]) {
  return /^[一二三四五六七八九十]+$/.test(cells[0] || "") &&
    Boolean(cells[1]) &&
    cells.slice(2).every((cell) => !cell)
}

export function isReimbursementSectionRow(row: ReimbursementMaterialTableRow) {
  return row.kind === "section" || isSectionCells(row.cells)
}

function isChineseSectionOrdinal(value: string) {
  return /^[一二三四五六七八九十]+$/.test(value)
}

export function getReimbursementSectionOrdinal(row: ReimbursementMaterialTableRow) {
  const firstCell = normalizeCell(row.cells[0])
  return isChineseSectionOrdinal(firstCell) ? firstCell : ""
}

export function getReimbursementSectionTitle(row: ReimbursementMaterialTableRow) {
  const ordinal = getReimbursementSectionOrdinal(row)
  const titleCells = ordinal ? row.cells.slice(1) : row.cells
  return titleCells.map(normalizeCell).find(Boolean) || ordinal || "分组"
}

function getColumnIndexByLabel(
  columns: ReimbursementMaterialTableColumn[],
  labels: string[],
  fallbackIndex: number
) {
  const matchedIndex = columns.findIndex((column) => labels.some((label) => column.label.includes(label)))
  if (matchedIndex >= 0) return matchedIndex
  return columns.length > fallbackIndex ? fallbackIndex : -1
}

export function getReimbursementCountryColumnIndex(columns: ReimbursementMaterialTableColumn[]) {
  return getColumnIndexByLabel(columns, ["国家", "地区"], 1)
}

export function getReimbursementCityColumnIndex(columns: ReimbursementMaterialTableColumn[]) {
  return getColumnIndexByLabel(columns, ["城市"], 2)
}

export type ReimbursementDisplayRow = {
  row: ReimbursementMaterialTableRow
  country: string
  countryRowSpan: number
  showCountryCell: boolean
  isCountryContinuation: boolean
}

export function buildReimbursementDisplayRows(
  rows: ReimbursementMaterialTableRow[],
  columns: ReimbursementMaterialTableColumn[]
): ReimbursementDisplayRow[] {
  const countryColumnIndex = getReimbursementCountryColumnIndex(columns)
  const displayRows: ReimbursementDisplayRow[] = rows.map((row) => ({
    row,
    country: "",
    countryRowSpan: 1,
    showCountryCell: true,
    isCountryContinuation: false,
  }))

  if (countryColumnIndex < 0) return displayRows

  let currentCountry = ""
  let currentCountryStartIndex: number | null = null

  rows.forEach((row, index) => {
    if (isReimbursementSectionRow(row)) {
      currentCountry = ""
      currentCountryStartIndex = null
      return
    }

    const country = normalizeCell(row.cells[countryColumnIndex])
    if (country) {
      currentCountry = country
      currentCountryStartIndex = index
      displayRows[index].country = country
      return
    }

    if (currentCountry && currentCountryStartIndex !== null) {
      displayRows[index].country = currentCountry
      displayRows[index].showCountryCell = false
      displayRows[index].isCountryContinuation = true
      displayRows[currentCountryStartIndex].countryRowSpan += 1
    }
  })

  return displayRows
}

function makeUniqueId(rawId: string | undefined, fallback: string, usedIds: Set<string>) {
  const base = normalizeSlug(rawId) || normalizeSlug(fallback) || fallback
  let next = base
  let suffix = 2
  while (usedIds.has(next)) {
    next = `${base}-${suffix}`
    suffix += 1
  }
  usedIds.add(next)
  return next
}

