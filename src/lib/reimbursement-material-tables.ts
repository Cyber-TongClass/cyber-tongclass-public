export function normalizeReimbursementTableDraft(
  input: Partial<ReimbursementMaterialTableDraft>
): ReimbursementMaterialTableDraft {
  const usedColumnIds = new Set<string>()
  const sourceColumns = input.columns?.length ? input.columns : livingExpenseColumns
  const columns: ReimbursementMaterialTableColumn[] = []
  sourceColumns.forEach((column, index) => {
    const label = normalizeCell(column.label)
    if (!label) return
    const width = normalizeCell(column.width)
    const nextColumn: ReimbursementMaterialTableColumn = {
      id: makeUniqueId(column.id, label || `column-${index + 1}`, usedColumnIds),
      label,
    }
    if (width) nextColumn.width = width
    columns.push(nextColumn)
  })

  const safeColumns = columns.length > 0 ? columns : [{ id: "column-1", label: "列 1" }]
  const usedRowIds = new Set<string>()
  const rows: ReimbursementMaterialTableRow[] = []
  const sourceRows = input.rows || []
  sourceRows.forEach((row, rowIndex) => {
    const cells = Array.from({ length: safeColumns.length }, (_, cellIndex) => normalizeCell(row.cells?.[cellIndex]))
    if (cells.every((cell) => !cell)) return
    const isSection = row.kind === "section" || isSectionCells(cells)
    const nextRow: ReimbursementMaterialTableRow = {
      id: makeUniqueId(row.id, `row-${rowIndex + 1}`, usedRowIds),
      cells,
    }
    if (isSection) {
      nextRow.kind = "section"
      nextRow.sectionLevel = Math.max(1, Math.min(3, Math.floor(row.sectionLevel || 1)))
    }
    rows.push(nextRow)
  })

  return {
    _id: input._id,
    slug: normalizeSlug(input.slug) || LIVING_EXPENSE_TABLE_SLUG,
    title: normalizeCell(input.title) || "未命名表格",
    description: normalizeCell(input.description),
    category: normalizeSlug(input.category) || ACADEMIC_EXCHANGE_REIMBURSEMENT_CATEGORY,
    columns: safeColumns,
    rows,
    isPublished: Boolean(input.isPublished),
  }
}

export function filterReimbursementRows(
  rows: ReimbursementMaterialTableRow[],
  columns: ReimbursementMaterialTableColumn[],
  query: string
) {
  const normalizedQuery = query.trim().toLowerCase()
  if (!normalizedQuery) return rows
  if (columns.some((column) => column.label.toLowerCase().includes(normalizedQuery))) return rows
  const includedIndexes = new Set<number>()
  let currentSectionIndex: number | null = null
  let currentCountryStartIndex: number | null = null
  const countryColumnIndex = getReimbursementCountryColumnIndex(columns)

  rows.forEach((row, index) => {
    const rowMatches = row.cells.some((cell) => cell.toLowerCase().includes(normalizedQuery))

    if (isReimbursementSectionRow(row)) {
      currentSectionIndex = index
      currentCountryStartIndex = null
      if (rowMatches) {
        includedIndexes.add(index)
        for (let nextIndex = index + 1; nextIndex < rows.length; nextIndex += 1) {
          if (isReimbursementSectionRow(rows[nextIndex])) break
          includedIndexes.add(nextIndex)
        }
      }
      return
    }

    const countryCell = countryColumnIndex >= 0 ? normalizeCell(row.cells[countryColumnIndex]) : ""
    if (countryCell) currentCountryStartIndex = index

    if (rowMatches) {
      if (currentSectionIndex !== null) includedIndexes.add(currentSectionIndex)
      if (currentCountryStartIndex !== null) includedIndexes.add(currentCountryStartIndex)
      includedIndexes.add(index)

      if (countryCell.toLowerCase().includes(normalizedQuery)) {
        for (let nextIndex = index + 1; nextIndex < rows.length; nextIndex += 1) {
          if (isReimbursementSectionRow(rows[nextIndex])) break
          const nextCountryCell = countryColumnIndex >= 0 ? normalizeCell(rows[nextIndex].cells[countryColumnIndex]) : ""
          if (nextCountryCell) break
          includedIndexes.add(nextIndex)
        }
      }
    }
  })

  return rows.filter((_, index) => includedIndexes.has(index))
}
